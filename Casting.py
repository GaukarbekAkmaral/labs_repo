
#Integers:
x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3

#Floats:
a = float(1)     # a will be 1.0
b = float(2.8)   # b will be 2.8
c = float("3")   # c will be 3.0
w = float("4.2") # w will be 4.2

#Strings:
d = str("s1") # d will be 's1'
f = str(2)    # f will be '2'
p = str(3.0)  # p will be '3.0'